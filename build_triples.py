def remove_trailing_punctuation(s):
    while True:
        found_change = False
        s = s.strip()
        if s == '':
            return s
        # leave trailing punctuation if the string ends in an initial, like ' A.'
        if re.search('\s[A-Z]\.$', s) != None:
            return s
        # leave trailing punctuation if the string ends in a known abbreviation.
        if s.endswith(' Co.') or \
           s.endswith(' Corp.') or \
           s.endswith(' Dept.') or \
           s.endswith(' Inc.'):
            return s
        if s[-1] in (',', '.',):
            found_change = True
            s = s[:-1]
        if s[-2:] in (' :', ' ;',):
            found_change = True
            s = s[:-2]
        if found_change == False:
            break
    return s

