def get_iso_8601_date(s):
    """
    first case: find 1 or 2 groups of four digits in a row, e.g.
    [1876]
    1916.
    [between 1934 and 1939]
    [1926?]
    1931.
    [1913-1929]
    [between 1930 and 1939?]

    second case: find three digits followed by a single dash, e.g.
    [192-]
    [193-]
    [193-]
    [192-?]

    third case: find two digits followed by two dashes. e.g.
    [19--?]
    [19--?]
    """
    
    dates = re.findall('[0-9]{4}', s)
    if len(dates) == 1 or len(dates) == 2:
        return '/'.join(dates)
    
    
    m = re.search('([0-9]{3})-', s)
    if m:
        return '{0}0/{0}9'.format(m.group(1))
    
    
    m = re.search('([0-9]{2})--', s)
    if m:
        return '{0}00/{0}99'.format(m.group(1))


